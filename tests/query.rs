mod utils;

use utils::tcount;

#[test]
fn test_local_queries() {
    tcount()
        .current_dir("tests/fixtures/")
        .env(
            "XDG_CONFIG_HOME",
            format!(
                "{}/tests/fixtures/xdg_config_home",
                env!("CARGO_MANIFEST_DIR")
            ),
        )
        .args(
            [
                "--format",
                "csv",
                "--whitelist",
                "Rust",
                "Go",
                "Ruby",
                "--query",
                "_test",
            ]
            .iter(),
        )
        .assert()
        .stdout(
            r"Group,Files,Tokens,Query(_test)
Rust,5,156,24
Go,1,52,0
Ruby,2,43,2
",
        )
        .success();
}

